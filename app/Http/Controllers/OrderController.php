    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $orders =  Order::all();
        foreach ($orders as $order){
            if($order->id == $id){
                $total =0;
                foreach ($order->products as $product){
                    $total += $product->price;
                }
                return View('pages.order.show',['order'=>$order,'total'=>$total]);
            }
        }
        return View();
    }

